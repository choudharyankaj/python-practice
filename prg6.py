# User se input (string)
num = input("Enter a number: ")

# Type Casting
num_int = int(num)
num_float = float(num)

# Print original and converted values
print("Original (string):", num)
print("Integer:", num_int)
print("Float:", num_float)

# Simple arithmetic
print("Integer + 5 =", num_int + 5)
print("Float + 5 =", num_float + 5)

