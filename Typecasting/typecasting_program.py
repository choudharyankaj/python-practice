a = 46
t = type(a)          # class <int>
print(t)

b = 46.3
t = type(b)          # class <float>
print(t)

c = "Ankaj"
t = type(c)          # class <str>
print(t)

d = True
t = type(d)          # class <bool>
print(t)

e = None
t = type(e)          # class <NoneType>
print(t)

r = "34.4"
s = float(r)         # string to float
t = type(s)
print(t)
