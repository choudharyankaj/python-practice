# Purpose: Practice type conversion without user input

num = 15.75

# Convert to string
num_str = str(num)
print("As string:", num_str)

# Convert to integer
num_int = int(num)
print("As integer:", num_int)

# Convert to float
num_float = float(num_int)
print("As float:", num_float)

# Type checks
print("Type of num_int:", type(num_int))
print("Type of num_float:", type(num_float))
