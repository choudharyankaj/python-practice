# Purpose: Practice boolean operations without user input

is_adult = True
has_ticket = False

# Logical AND
print("Can enter (AND):", is_adult and has_ticket)

# Logical OR
print("Can enter (OR):", is_adult or has_ticket)

# Logical NOT
print("Not adult:", not is_adult)

# Type check
print("Type of is_adult:", type(is_adult))
